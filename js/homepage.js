// ÿ™ÿßÿ®ÿπ ÿßÿ™ÿµÿßŸÑ ŸáŸàÿ¥ŸÖŸÜÿØ
async function connectWallet() {
    try {
        // ÿ®ÿ±ÿ±ÿ≥€å ÿßÿ™ÿµÿßŸÑ ŸÖŸàÿ¨ŸàÿØ
        if (window.contractConfig && window.contractConfig.contract && window.contractConfig.address) {
            return window.contractConfig;
        }
        
        // ÿßÿ≥ÿ™ŸÅÿßÿØŸá ÿßÿ≤ ÿ™ÿßÿ®ÿπ smartConnect ÿßÿ≤ walletconnect-handler
        if (typeof smartConnect === 'function') {
            const result = await smartConnect();
            if (result) {
                // ÿß⁄Øÿ± smartConnect ŸÖŸàŸÅŸÇ ÿ®ŸàÿØÿå contractConfig ÿ®ÿß€åÿØ ÿ™ŸÜÿ∏€åŸÖ ÿ¥ÿØŸá ÿ®ÿßÿ¥ÿØ
                if (window.contractConfig && window.contractConfig.contract) {
                    return window.contractConfig;
                }
            }
        }
        
        // fallback ÿ®Ÿá ÿ±Ÿàÿ¥ ŸÇÿØ€åŸÖ€å
        if (typeof window.ethereum !== 'undefined') {
            try {
                const accounts = await window.ethereum.request({
                    method: 'eth_requestAccounts'
                });
                
                if (accounts && accounts.length > 0) {
                    await window.initializeWeb3();
                    
                    // ÿ®ÿ±ÿ±ÿ≥€å ÿß€åŸÜ⁄©Ÿá ÿ¢€åÿß contractConfig ÿ™ŸÜÿ∏€åŸÖ ÿ¥ÿØŸá ÿßÿ≥ÿ™
                    if (window.contractConfig && window.contractConfig.contract) {
                        return window.contractConfig;
                    }
                }
            } catch (error) {
                // console.error('MetaMask connection failed:', error);
            }
        }
        
        return null;
        
    } catch (error) {
        // console.error('Wallet connection failed:', error);
        return null;
    }
}

